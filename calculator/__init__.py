"""
Calculator module that provides basic arithmetic operations.
"""

def add(a,b):
    """
    Add two numbers.
    """
    return a + b

def multi(a,b):
    """
    Multiply two numbers.
    """
    return a * b

def sub(a,b):
    """
    Subtract one number from another.
    """
    return a - b

def div(a,b):
    """
    Divide one number from another.
    """
    return a / b
